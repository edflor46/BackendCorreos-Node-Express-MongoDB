const { Router } = require('express');
const { sendCorreo, getCorreos, getCorreo } = require('../controllers/correos.controller');

/*==========ROUTER==========*/
const router = Router();

/* -------------------------------------------------------------------------- */
/*                                 SEND CORREO                                */
/* -------------------------------------------------------------------------- */
router.post('/',
sendCorreo); 

/* -------------------------------------------------------------------------- */
/*                                   GET ALL                                  */
/* -------------------------------------------------------------------------- */
router.get('/', getCorreos); 

/* -------------------------------------------------------------------------- */
/*                                 GET FOR ONE                                */
/* -------------------------------------------------------------------------- */
router.get('/:id', getCorreo); 

module.exports = router;